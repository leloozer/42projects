#include "ft_ls.h"

int	ft_intlen(int nbr)
{
	int len;

	len = 0;
	if (nbr == 0)
		return (1);
	while (nbr > 0)
	{
		nbr = nbr / 10;
		len++;
	}
	return (len);
}
