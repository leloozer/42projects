#include "libft.h"

int		ft_isspace(int c)
{
	if (c == ' ' || c == '\t' || c == '\n' || c == '\v' ||
		c == '\f' || c == '\r' || c == '%' || c == ':')
		return (1);
	else
		return (0);
}
